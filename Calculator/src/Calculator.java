
public class Calculator {
	public long add (long a, long b) {
		return a + b;
	} // end Method add -- 
	
	public long subtract (long a, long b) {
		return a - b;
	} // end Method subtract
	
	public long multiply (long a, long b) {
		return a * b;
	} // end Method multiply
	
	public long divide (long a, long b) {
		return a / b;
	} // end Method divide
}
